/*
No JavaScript temos os operadores aritméticos, de atribuição, de comparação, lógicos, bitwise e unários, etc...
*/
var num = 0; // 1
num = num + 2;
num = num * 3;
num = num / 2;
num++;
num--;

num += 1; // 2
num -= 2;
num *= 3;
num /= 2;
num %= 3;

console.log("num == 1 : " + (num == 1)); // 3
console.log("num === 1 : " + (num === 1));
console.log("num != 1 : " + (num != 1));
console.log("num > 1 : " + (num > 1));
console.log("num < 1 : " + (num < 1));
console.log("num >= 1 : " + (num >= 1));
console.log("num <= 1 : " + (num <= 1));

console.log("true && false : " + (true && false)); // 4
console.log("true || false : " + (true || false));
console.log("!true : " + !true);

